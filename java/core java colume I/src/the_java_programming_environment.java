// java jargon

// JOK
// java development kit
// the software for programmers who want to write java programs
//   JRE
//   java runtime environment
//   the software for consumers who want to run java programs
// SE
// standard edition
// the java platform for use on desktops and simple server applications
//   EE
//   enterprise edition
//   the java platform for complex server applications
// ME
// micro edtion
// the java platform for use on cell phones and other small devices

// pick the right JDK

// you want the JDK, not the JRE
// windows or linux: choose x86 for 32 bit, x64 for 64 bit
// linux: pick the .tar.gz version

// using the command-line tools

// using java compiler in terminal
//   javac welcome.java
//   java welcome
// the javac program is the java compiler
//   it compilers the file welcome.java into the file welcome.class
//   the java program launches the java virtual machine
//   it executes the bytecodes that the compiler placed in the class file
